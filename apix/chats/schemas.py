from marshmallow import Schema, fields

class UserSchema(Schema):
    id = fields.Int(dump_only=True)
    username = fields.String()
    first_name = fields.String()
    last_name = fields.String()
    avatar = fields.String()
    mobile = fields.String()
    email = fields.String()
    gender = fields.String()
    bio = fields.String()
    address = fields.String()
    worship_location = fields.String()
    ministry_1 = fields.String()
    ministry_2 = fields.String()
    ministry_3 = fields.String()
    birth_date = fields.String()
    is_bot = fields.Boolean()
    is_verified = fields.Boolean()
    is_used = fields.Boolean()
    is_admin = fields.Boolean()
    is_staff = fields.Boolean()
    is_active = fields.Boolean()
    date_joined = fields.DateTime()
    is_deleted = fields.Boolean()
    modified_at = fields.DateTime()
    deleted_at = fields.DateTime()
    restored_at = fields.DateTime()


class ChatSchema(Schema):
    id = fields.Int(dump_only=True)
    name = fields.String()
    description = fields.String()
    pic = fields.String()
    pic1 = fields.String()
    pic2 = fields.String()
    is_bot = fields.Boolean()
    is_private = fields.Boolean()
    last_content = fields.String()
    unread_messages = fields.Int()
    starter_id = fields.Int()
    created_at = fields.DateTime()
    modified_at = fields.DateTime()
    is_deleted = fields.Boolean()
    deleted_at = fields.DateTime()
    restored_at = fields.DateTime()
    starter = fields.Nested(UserSchema())
    participants = fields.List(fields.Nested(UserSchema()), dump_only=True)

class ChatItemSchema(Schema):
    id = fields.Int(dump_only=True)
    name = fields.String()
    description = fields.String()
    pic = fields.String()
    pic1 = fields.String()
    pic2 = fields.String()
    is_bot = fields.Boolean()
    is_private = fields.Boolean()
    last_content = fields.String()
    unread_messages = fields.Int()
    starter_id = fields.Int()
    created_at = fields.DateTime()
    modified_at = fields.DateTime()
    is_deleted = fields.Boolean()
    deleted_at = fields.DateTime()
    restored_at = fields.DateTime()
    starter = fields.Nested(UserSchema())

class MessageSchema(Schema):
    id = fields.Int(dump_only=True)
    idx = fields.String()
    content = fields.String()
    attachment = fields.String()
    chat_id = fields.Int()
    sender_id = fields.Int()
    created_at = fields.DateTime()
    is_read = fields.Boolean()
    read_at = fields.DateTime()
    is_deleted = fields.Boolean()
    deleted_at = fields.DateTime()
    sender = fields.Nested(UserSchema(), dump_only=True)